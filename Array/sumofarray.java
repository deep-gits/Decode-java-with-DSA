package Array;

public class sumofarray {
    public static void main(String[] args) {
        int[] arr= {2,3,4,2,23,2,3,122,45};
        int n=arr.length;
        int sum=0;
        for (int i = 0; i < n; i++) {
            sum+=arr[i];

        }
        System.out.println(sum);
    }
}





































































